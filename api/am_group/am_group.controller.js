const { GroupInsert, GroupView, GroupUpdate } = require('../am_group/am_group.services')
module.exports = {
    GroupInsert: (req, res) => {
        const body = req.body;
        GroupInsert(body, (err, result) => {
            if (err) {
                return res.status(200).json({
                    success: 0,
                    message: err
                });
            }
            return res.status(200).json({
                success: 1,
                insertid: result.insertId,
                message: "Group inserted successfully"
            })
        })
    },
    GroupView: (req, res) => {

        GroupView((err, results) => {
            if (err) {
                return res.status(200).json({
                    success: 0,
                    message: err
                })
            }
            if (results === 0) {
                return res.status(200).json({
                    success: 1,
                    message: "No Records"
                })
            }
            return res.status(200).json({
                success: 2,
                data: results
            })

        })
    },
    GroupUpdate: (req, res) => {
        const body = req.body;
        GroupUpdate(body, (err, results) => {
            if (err) {
                return res.status(200).json({
                    success: 0,
                    message: err
                })
            }
            if (results === 0) {
                return res.status(200).json({
                    success: 1,
                    message: "No record found"

                })
            }
            return res.status(200).json({
                success: 2,
                message: "Group data Updated successfully"
            })
        })
    },
}