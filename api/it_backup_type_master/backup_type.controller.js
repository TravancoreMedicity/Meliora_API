const { BackupTypeInsert, BackupTypeView, BackupTypeUpdate, getBackupType } = require('../it_backup_type_master/backup_type.service')
module.exports = {
    BackupTypeInsert: (req, res) => {
        const body = req.body;
        BackupTypeInsert(body, (err, result) => {
            if (err) {
                return res.status(200).json({
                    success: 0,
                    message: err
                });
            }
            return res.status(200).json({
                success: 1,
                message: "Backup Type inserted successfully"
            })
        })
    },
    BackupTypeView: (req, res) => {

        BackupTypeView((err, results) => {
            if (err) {
                return res.status(200).json({
                    success: 0,
                    message: err
                })
            }
            if (results === 0) {
                return res.status(200).json({
                    success: 1,
                    message: "No Records"
                })
            }
            return res.status(200).json({
                success: 2,
                data: results
            })

        })
    },
    BackupTypeUpdate: (req, res) => {
        const body = req.body;

        BackupTypeUpdate(body, (err, results) => {
            if (err) {
                return res.status(200).json({
                    success: 0,
                    message: err
                })
            }
            if (results === 0) {
                return res.status(200).json({
                    success: 1,
                    message: "No record found"

                })
            }
            return res.status(200).json({
                success: 2,
                message: "Backup Type Updated successfully"
            })
        })
    },
    getBackupType: (req, res) => {
        getBackupType((err, results) => {
            if (err) {
                return res.status(200).json({
                    success: 0,
                    message: err
                })
            }
            if (results === 0) {
                return res.status(200).json({
                    success: 1,
                    message: "No Records"
                })
            }
            return res.status(200).json({
                success: 2,
                data: results
            })

        })
    },
}