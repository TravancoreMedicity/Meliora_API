const { simOperatorInsert, simOperatorView, simOperatorUpdate, getsimOperator } = require('./sim_operators_service')
module.exports = {
    simOperatorInsert: (req, res) => {
        const body = req.body;
        simOperatorInsert(body, (err, result) => {
            if (err) {
                return res.status(200).json({
                    success: 0,
                    message: err
                });
            }
            return res.status(200).json({
                success: 1,
                message: "Backup Type inserted successfully"
            })
        })
    },
    simOperatorView: (req, res) => {

        simOperatorView((err, results) => {
            if (err) {
                return res.status(200).json({
                    success: 0,
                    message: err
                })
            }
            if (results === 0) {
                return res.status(200).json({
                    success: 1,
                    message: "No Records"
                })
            }
            return res.status(200).json({
                success: 2,
                data: results
            })

        })
    },
    simOperatorUpdate: (req, res) => {
        const body = req.body;

        simOperatorUpdate(body, (err, results) => {
            if (err) {
                return res.status(200).json({
                    success: 0,
                    message: err
                })
            }
            if (results === 0) {
                return res.status(200).json({
                    success: 1,
                    message: "No record found"

                })
            }
            return res.status(200).json({
                success: 2,
                message: "Backup Type Updated successfully"
            })
        })
    },
    getsimOperator: (req, res) => {
        getsimOperator((err, results) => {
            if (err) {
                return res.status(200).json({
                    success: 0,
                    message: err
                })
            }
            if (results === 0) {
                return res.status(200).json({
                    success: 1,
                    message: "No Records"
                })
            }
            return res.status(200).json({
                success: 2,
                data: results
            })

        })
    },
}